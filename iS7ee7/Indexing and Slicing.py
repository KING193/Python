# Indexing and Slicing:

message = "s7ee7 loves python"
		  #012345678910

name = message[0:5]
print(name)# => s7ee7

word = message[6:11]
print(word)# => loves

alls = message[6:]
print(alls)# => loves python

fox = "s7ee7 loves python c# java javascript"
print(fox[12:])

name = "mohammed khaled"

first_name = name[0:8]
last_name = name[9:]

