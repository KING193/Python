# Python Object Oriented Programming

name = "Simo"
print(type(name))# => <class 'str'>

name = 6
print(type(name))# => <class 'int'>

name = True
print(type(name))# => <class 'bool'>

name = 7.7
print(type(name))# => <class 'float'>

def greet():
    print("hello")

greet()
print(type(greet))# => <class 'function'>